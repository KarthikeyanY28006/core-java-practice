public class Literals {
    public static void main(String[] args) {

        // Type Conversion (Automatic)
        int a = 100;
        double b = a;

        // Type Casting (Manual)
        double x = 45.67;
        int y = (int) x;

        System.out.println("Conversion: " + b);
        System.out.println("Casting: " + y);
    }
}
