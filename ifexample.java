public class ifexample {
    public static void main (String [] args) {
        int a = 5;
        if ( a < 0)
        {
            System.out.println("a is a positive number.");  
            }
        else 
        {
            System.out.println("a is a negative number.");
        }
 }
}
// The if statement is a control flow statement that allows you to execute a block of code only if a specified condition is true. The condition is evaluated, and if it is true, 
// the code block inside the if statement is executed. 
// If the condition is false, the code block is skipped.          
